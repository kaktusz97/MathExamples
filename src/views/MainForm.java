    private void btCheckAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCheckAnswerActionPerformed
        if (tfAnswer.getText().
                length() > 0) {
            int result = getFormattedAnswer();
            if (result == hiddenNumber) {
                setFormAfterCorrectAnswer();
            } else {
                setFormAfterWrongAnswer();
            }
        }
    }//GEN-LAST:event_btCheckAnswerActionPerformed

