    private void setupEquation() {
        equation = Equation.getRandomEquation();
        fillFormWithEqutation(equation);
        hideRandomField();
    }

    private void fillFormWithEqutation(Equation eq) {
        tfFirstNumber.setText(String.valueOf(eq.getFirstNumber()));
        tfSecondNumber.setText(String.valueOf(eq.getSecondNumber()));
        tfOperator.setText(String.valueOf(eq.getOperator()));
        tfResult.setText(String.valueOf(eq.getResult()));
        tfAnswer.requestFocus();
    }

    private void hideRandomField() {
        String hide = "?";
        int chance = random.nextInt(0, 3);
        switch (chance) {
            case 0:
                tfFirstNumber.setText(hide);
                hiddenNumber = equation.getFirstNumber();
                hiddenTextField = tfFirstNumber;
                break;
            case 1:
                tfSecondNumber.setText(hide);
                hiddenNumber = equation.getSecondNumber();
                hiddenTextField = tfSecondNumber;
                break;
            case 2:
                tfResult.setText(hide);
                hiddenNumber = equation.getResult();
                hiddenTextField = tfResult;
                break;
        }
    }

    private int getFormattedAnswer() {
        String result = tfAnswer.getText();
        result = result.replaceAll("\\s", "");
        int answer = Integer.parseInt(result);
        return answer;
    }

    private void setFormAfterCorrectAnswer() {
        btNewExample.setEnabled(true);
        btCheckAnswer.setEnabled(false);
        tfAnswer.setEnabled(false);
        hiddenTextField.setText(String.valueOf(hiddenNumber));
        correctAnswerMessage = new CorrectAnswerMessage(this, false);
        correctAnswerMessage.setVisible(true);
    }

    private void setFormAfterWrongAnswer() {
        tfAnswer.setText("");
        tfAnswer.requestFocus();
        wrongAnswerMessage = new WrongAnswerMessage(this, false);
        wrongAnswerMessage.setVisible(true);
    }

